package tax.domain;

public class Slab {
    private Long first;
    private Long second;
    private Long third;
    private Long forth;
    private Long fifth;
    private Long sixth;
    private Long total;

    public Long getFirst() {
        return first;
    }

    public void setFirst(Long first) {
        this.first = first;
    }

    public Long getSecond() {
        return second;
    }

    public void setSecond(Long second) {
        this.second = second;
    }

    public Long getThird() {
        return third;
    }

    public void setThird(Long third) {
        this.third = third;
    }

    public Long getForth() {
        return forth;
    }

    public void setForth(Long forth) {
        this.forth = forth;
    }

    public Long getFifth() {
        return fifth;
    }

    public void setFifth(Long fifth) {
        this.fifth = fifth;
    }

    public Long getSixth() {
        return sixth;
    }

    public void setSixth(Long sixth) {
        this.sixth = sixth;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }
}
